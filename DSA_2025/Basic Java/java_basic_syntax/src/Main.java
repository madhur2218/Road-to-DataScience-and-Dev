public class Main {
    public static void main(String[] args) {
        //Primitive Data types

        //Integers
        int num1 = 10;
        System.out.println(num1);

        //Float
        float num2 = 40f;
        System.out.println(num2);

        //Double
        double num3 = 30.45d;
        System.out.println(num3);

        //Char
        char char1= 'c';
        System.out.println(char1);

        //Boolean
        boolean isTrue = true;
        System.out.println(isTrue);

        //String
        String str1 = "Madhur";
        System.out.println(str1);

    }
}
